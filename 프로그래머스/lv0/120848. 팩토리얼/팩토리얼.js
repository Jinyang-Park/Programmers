function solution(n) {
    // 2! = 2 x 1  = 2 팩토리얼 2
    // 3! = 3*2*1  = 6 팩토리얼 3
    // 4! = 4*3*2*1  = 24 팩토리얼 4
    // 7! = 7*6*5*4*3*2*1 = 팩토리얼 7
    
    let count = 1;
    for(let i = 1; i <= 11; i++){
        count *= i;
        // count = count * i
        // 1*2*3*4*
        // 1*1= 1
        // 1*2= 2
        // 2*3 = 6
        // 6*4 = 24
        // 24*5 = 120
        // 120 * 6 = 720
        if(count > n){
            return i - 1
        } 
    }
}